function d_determinant = dQUESTmethod(K,s)
%% Function returns derivation of determinant of matrix (K-s*I_4x4)
    d_d1 = -(K(2,3)*K(3,4)*K(4,2) + K(2,4)*K(3,2)*K(4,3)) + (K(4,2)*(K(3,3)-s)*K(2,4) + K(4,3)*K(3,4)*(K(2,2)-s) + (K(4,4)-s)*K(3,2)*K(2,3)) + (K(1,1)-s)*(K(4,2)*K(2,4) + K(4,3)*K(3,4) + K(3,2)*K(2,3)) + (-(K(1,1) + K(2,2)) + 2*s)*(K(3,3)*K(4,4) - (K(3,3) + K(4,4))*s + s^2) + (K(1,1)*K(2,2) -(K(1,1) + K(2,2))*s +s^2)*(- (K(3,3) + K(4,4)) + 2*s);
    
    d_d2 = -K(1,2)*(-K(2,1)*(K(4,4)-s) - K(2,1)*(K(3,3)-s) + K(4,1)*K(2,4) + (K(3,1)*K(2,3)));
    
    d_d3 = K(1,3)*(-K(2,1)*K(3,2) - K(3,4)*K(4,1) + K(3,1)*(K(2,2)-s) + K(3,1)*(K(4,4)-s));
    
    d_d4 = -K(1,4)*(-K(4,1)*(K(3,3)-s) - K(4,1)*(K(2,2)-s) + K(4,2)*K(2,1) + K(4,3)*K(3,1));
    
    d_determinant = d_d1 + d_d2 + d_d3 + d_d4;

end